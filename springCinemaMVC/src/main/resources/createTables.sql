  CONSTRAINT "PK_CINEMA_AUDITORIUM" PRIMARY KEY ("ID")
);
CREATE SEQUENCE CINEMA_AUDITORIUM_SEQ MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1;

