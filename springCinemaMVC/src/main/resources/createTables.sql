CREATE TABLE CINEMA_EVENT
(
  "ID" NUMBER(19,0) NOT NULL,
  "NAME" VARCHAR2(255) NOT NULL,
  BASE_PRICE NUMBER(31,10) NOT NULL,
  RATIN_SYS_NAME VARCHAR2(255) NOT NULL,
  CONSTRAINT "PK_CINEMA_EVENT" PRIMARY KEY ("ID")
);
CREATE SEQUENCE CINEMA_EVENT_SEQ MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1;

CREATE TABLE CINEMA_AIR_DATES
(
  EVENT_ID NUMBER(19,0) NOT NULL,
  "DATE" TIMESTAMP NOT NULL
);

CREATE TABLE CINEMA_EVENT_AUDITORIUM_LINK
(
  EVENT_ID NUMBER(19,0) NOT NULL,
  "DATE" TIMESTAMP NOT NULL,
  AUDITORIUM_ID NUMBER(19,0) NOT NULL
);

CREATE TABLE CINEMA_AUDITORIUM
(
  "ID" NUMBER(19,0) NOT NULL,
